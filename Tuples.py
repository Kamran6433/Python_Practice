# A Tuple is a data structure where you can store different values. Very similar to list.

# Tuples are created similarly to lists but instead of [] we use ()
# Lists are MUTABLE
# Tuples are IMMUTABLE which means unchanging over time or unable to be changed.
coordinates = (9, 3)
print(coordinates)

names = ("Harry", "Kamran")
print(names)

mix = (False, True, 89, 9.999, "Pelitia")
print(mix)

# you're also able to access all the indexes of the tuple however you cannot MODIFY the tuple at any stage.
